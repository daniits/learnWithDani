// What is Control structures and Conditional structures ?
// Control structures and conditional structures are fundamental concepts in programming 
// that help control the flow of execution in a program. Here's an explanation of each:


// Control Structures
// Control structures are constructs in programming that dictate the flow of control or the sequence in which statements are executed. 
// The three main types of control structures are:

// Sequential Control:
// The default mode where instructions are executed one after another in the order they appear.

// Selection Control (Conditional Statements):
// This structure allows the program to make decisions based on certain conditions. 
// The flow of the program can change depending on whether the condition is true or false.
//  Examples include if, else, switch statements.


// Loop Control: 
// Allows the program to repeat a block of code as long as a certain condition is true. 
// Examples include for, while, and do-while loops.



// Condiational Structure
// Conditional structures, a subset of control structures, enable decision-making in a program by 
// allowing certain blocks of code to be executed only if specified conditions are met. 
// These are used to perform different actions based on different conditions. 
// The most common conditional structures are:

// if Statement: Executes a block of code if a specified condition is true.
if (condition) {
    // code to execute if condition is true
}

// if-else Statement: Provides an alternative block of code to execute if the condition is false.


// else if Statement: Checks multiple conditions.

// switch Statement: Used to select one of many code blocks to be executed.



