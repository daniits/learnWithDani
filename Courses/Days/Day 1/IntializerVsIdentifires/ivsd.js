// What is Intializer? 
//  The initializer assigns an initial value to a variable when it's declared.
// Example 
let age = 25;  // '25' is the initializer.
const name = "John";  // "John" is the initializer.


// What is Decelration?
// A declaration defines the type or nature of the variable or object but does not always assign a value.
let value;  // Declaration of the variable 'value' without assigning a value.
// const valuename;  // Declaration of the constant 'valuename' (this will throw an error because constants require initialization at the time of declaration).
